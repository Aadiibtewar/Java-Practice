package variables_and_datatypes;

class DeclareAndPrint{
    public static void main(String[] args) {
        byte by = 124;
        short sh = 55;
        char ch ='f';
        int a = 10;
        long l = 100l;
        float b = 10.2f;
        double d = 44.11;
        boolean bool = true;

        System.out.println(by+"\n"+sh+"\n"+ch+"\n"+a+"\n"+l+"\n"+"\n"+b+"\n"+d+"\n"+bool);
    }
}